export function getProblemsByCategory(category: string, difficulty?: string): DSAProblem[] {
  let problems = category === 'random' 
    ? dsaProblems 
    : dsaProblems.filter(p => p.category === category);
  
  if (difficulty) {
    problems = problems.filter(p => p.difficulty === difficulty);
  }
  
  // Shuffle
  return problems.sort(() => Math.random() - 0.5);
}
